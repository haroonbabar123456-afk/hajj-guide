let currentDay = "8";
let userProgress = JSON.parse(localStorage.getItem('hajjProgress')) || {};

function init() {
    renderContent(currentDay);
    setupTabs();
    updateProgressBar();
}

function setupTabs() {
    const tabs = document.querySelectorAll('.tab-btn');
    tabs.forEach(tab => {
        tab.addEventListener('click', () => {
            tabs.forEach(t => t.classList.remove('active'));
            tab.classList.add('active');
            currentDay = tab.getAttribute('data-day');
            renderContent(currentDay);
        });
    });
}

function renderContent(day) {
    const data = hajjData[day];
    const contentArea = document.getElementById('content-area');
    
    let html = `
        <div class="card">
            <h3>${data.title}</h3>
            <p style="margin-bottom: 15px; font-weight: bold; color: #1c6451;">مقام: ${data.location}</p>
            <div class="task-list">
    `;

    data.tasks.forEach(task => {
        const isChecked = userProgress[task.id] ? 'checked' : '';
        const completedClass = userProgress[task.id] ? 'completed' : '';
        const typeLabel = {
            "fard": "فرض",
            "wajib": "واجب",
            "sunnah": "سنت"
        }[task.type];

        html += `
            <div class="task-item ${completedClass}" onclick="toggleTask('${task.id}')">
                <input type="checkbox" id="${task.id}" ${isChecked}>
                <div class="ritual-icon-bubble">${task.icon}</div>
                <div class="task-text-content">
                    <span class="badge badge-${task.type}">${typeLabel}</span>
                    <span class="ritual-text">${task.text}</span>
                </div>
            </div>
        `;
    });

    html += `
            </div>
            <div class="note"><strong>اہم بات:</strong> ${data.notes}</div>
        </div>
    `;

    contentArea.innerHTML = html;
}

window.toggleTask = function(taskId) {
    userProgress[taskId] = !userProgress[taskId];
    localStorage.setItem('hajjProgress', JSON.stringify(userProgress));
    
    // Smooth update
    const checkbox = document.getElementById(taskId);
    if(checkbox) checkbox.checked = userProgress[taskId];
    
    // Update visual state and progress bar
    renderContent(currentDay);
    updateProgressBar();
};

function updateProgressBar() {
    const allTasks = [];
    Object.values(hajjData).forEach(day => {
        day.tasks.forEach(task => allTasks.push(task.id));
    });

    const total = allTasks.length;
    const completed = allTasks.filter(id => userProgress[id]).length;
    const percent = Math.round((completed / total) * 100);

    document.getElementById('progress-fill').style.width = percent + '%';
    document.getElementById('progress-percent').innerText = percent + '%';

    // Update stats bar
    const counts = { fard: 0, wajib: 0, sunnah: 0 };
    Object.values(hajjData).forEach(day => {
        day.tasks.forEach(task => counts[task.type]++);
    });

    document.getElementById('stat-fard').innerText = counts.fard;
    document.getElementById('stat-wajib').innerText = counts.wajib;
    document.getElementById('stat-sunnah').innerText = counts.sunnah;
}


document.addEventListener('DOMContentLoaded', () => {
    init();
    
    // Register Service Worker for PWA
    if ('serviceWorker' in navigator) {
        window.addEventListener('load', () => {
            navigator.serviceWorker.register('sw.js')
                .then(registration => {
                    console.log('SW registered: ', registration);
                })
                .catch(registrationError => {
                    console.log('SW registration failed: ', registrationError);
                });
        });
    }
});
